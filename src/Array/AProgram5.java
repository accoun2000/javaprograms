package Array;

public class AProgram5
{
    public static void main(String[] args) {
        int[] arr = new int[]{1,2,3,4,5};
        System.out.println("Elements of given array:");
        for(int i = 0;i<arr.length;i++)
            {
                System.out.println(arr[i]+" ");
            }

        }
    }

